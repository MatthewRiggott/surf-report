  def self.parse_regions
    new_england_beaches.each do |region, info|
      state = info["state"]
      info.each do |key, value|
        if value.class == Hash
          name = key
          msw_id = value["id"]
          url = value["url"]
          if !Location.find_by(msw_id: msw_id)
            Location.find_or_create_by(msw_id: msw_id, name: name, state: state, url: url)
          end
        end
      end
    end
  end

  def self.generate_empty_forecasts
    Location.all.each do |location|
      create_forecast_for location
    end
  end

  def self.create_forecast_for(location)
    (-3..5).each do |day|
      [8,14].each do |hour|
        Forecast.find_or_create_by(location: location, time: hour, day: day)
      end
    end
  end
